
class Mailing():
    
    def __init__(self, cost, track, to_address, from_address):
        self.cost = cost
        self.track = track
        self.to_address = to_address
        self.from_address = from_address
            
    def cost(self):
        return self.cost
    
    def track(self):
        return self.track
        
    def to_address(self):
        return self.to_address
        
    def from_address(self):
        return self.from_address
    
    


        
    
