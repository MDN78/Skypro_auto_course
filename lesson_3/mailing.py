from address import Address

class Mailing(Address):
    
    def cost(cost):
        return cost
    
    def track(track):
        return track
        
    def to_address(self):
        to_address = Address.address(self)
        return to_address
        
    def from_address(self):
        from_address = Address.address(self)
        return from_address
    
    


        
    
